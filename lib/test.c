int	main(int ac, char **av, char **env)
{
	char **newtab = NULL;
	int i = -1;
	while (env[++i])
		newtab = expand_tab(newtab, str("%1s", env[i]));
	newtab = pop_entry(newtab, "PWD=");
	put("%-t", newtab);
	free_tab(newtab);
}

