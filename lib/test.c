char	**pop_entry_v3(char **env, char *to_pop)
{
	char	**new_tab;
	char	**tab_ptr;
	int i = 0;

	if (!env || !to_pop)
		return (env);
	new_tab = NULL;
	tab_ptr = env;
	while (tab_ptr[i])
	{
		// ft_print_cat(i, tab_ptr[i], 0b01);
		if (tab_ptr[i][0] == to_pop[0] && !find_str(tab_ptr[i], to_pop))
		{
			put(ERR3"THIS GOT POPPED\n");
			free_s(tab_ptr[i]);
			i++;
			continue ;
		}
		new_tab = expand_tab(new_tab, tab_ptr[i]);
		i++;
	}
	free_s(env);
	return (new_tab);
}


